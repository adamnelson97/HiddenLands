package architecture;

/*
 * The difficulty is as follows:
 * Easy: One Level
 * Medium: Two Levels
 * Hard: Three Levels, one Boss Battle
 * HARDCORE: Four Levels, one hit point, two Boss Battles
 */

/**
 * <h1>Difficulty Enum</h1>
 * The difficulty tracks how many levels the player must complete
 * to 'beat' the game.
 * @author Adam Nelson
 * @version 1.0
 * @since 2017-10-31
 *
 */
public enum Difficulty {
	EASY, MEDIUM, HARD, HARDCORE;
}